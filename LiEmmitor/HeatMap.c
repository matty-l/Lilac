/** Returns the color in the heatmap closest to given temperature */
int find_color( const int temp ){
	int low, high,heat,index;
	low = 0;
	high = 782;
	index = 0;
		
	/* Binary search for closest temp */
	while (low <= high){
	
		index = low + (high-low) / 2;
		heat = colortable[index][0];
		
		if (heat > temp)
			high = index-1;
		else if (heat < temp)
			low = index+1;
		else
			break;
		
	}
	
	return colortable[index+(rand()%2)][1];
	
}


